// Default and abstract methods in interfaces are inherited like Instance
// methods. However when supertypes of a class or interface provide multiple
// default methods with the same signature, the java compiler follows
// inheritance rules to resolve the name conflict.

// Instance methods are prefered over interface default methods.

class Horse {
    public String identifyMyself() {
        return "I am a horse.";
    }
}
interface Flyer {
    default public String identifyMyself() {
        return "I am able to fly.";
    }
}
interface Mythical {
    default public String identifyMyself() {
        return "I am a mythical creature.";
    }
}
class Pegasus extends Horse implements Flyer, Mythical {
    public static void main(String... args) {
        Pegasus myApp = new Pegasus();
        System.out.println(myApp.identifyMyself());
    }
}
